const sets = require('../sets');
const icons = sets.map(set => require(`../sets/${set}`));

let out = `// This file is autogenerated by "make build_types", do not edit it.

export type Sets = '${sets.join("' | '")}';
`;

for (let i = 0; i < sets.length; i++) {
  const set = sets[i];
  const iconList = icons[i];
  out += `
export type icons_${set} = '${iconList.join("' | '")}';
`;
}

out += `
export type Icon =
  ${sets.map(set => `| {set: '${set}', icon: icons_${set}}`).join('\n  ')}
  ;
`;

require('fs').writeFileSync('src/types.ts', out);
